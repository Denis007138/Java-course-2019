package lesson4.classwork;

public class MassiveUtils {
    public static void printDoubleArray(int[][] mass) {
        for (int i = 0; i < mass.length; i++) {
            for (int j = 0; j < mass[i].length; j++) {
                System.out.print(mass[i][j] + " ");


            }
        }
    }
    public static void printDoubleArray(char[][] mass) {
        for (int i = 0; i < mass.length; i++) {
            for (int j = 0; j < mass[i].length; j++) {
                System.out.print(mass[i][j] + " ");


            }
        }
    }
}